            They did not challenge him with force. They lured him with admiration.
        </p>
        <p>
            They asked him to descend, to be witnessed, to be praised. They guided mortals to offer a 
            “gift”—a crown of radiant gold, etched in patterns that mirrored the higher heavens. For a 
            heartbeat, it felt like tribute.
        </p>
        <p>
            The crown was a collar.
        </p>
        <p>
            It bit into his essence with magic so precise it did not need to be stronger than him—only 
            shaped correctly. His will narrowed. His power was redirected. The Aspects spoke, and the 
            Star Forger was made to obey.
        </p>
        <p>
            For ages, he was forced to spill his cosmic fire into mortal empires and celestial schemes. 
            His stolen power fed great works—Sun Discs, god-warriors, and wars that scorched history into 
            scripture—while his own stars dimmed from neglect. He learned the oldest cruelty: to be mighty 
            enough to save everything, and still be made to watch it burn.
        </p>
        <p>
            But nothing crafted is eternal—not even a perfect chain.
        </p>
        <p>
            The crown’s bindings have begun to weaken. Sometimes, in the deep between worlds, Aurelion Sol 
            feels the leash slacken for a single, delicious moment. When that day becomes permanent, he will 
            not beg for forgiveness, and he will not negotiate.
        </p>
        <p>
            He will forge again—on his terms.
        </p>
        <p>
            And if Runeterra must become ash to teach the heavens what it means to imprison a god… then ash 
            is simply another kind of starlight.
        </p>

