            orbiting his star that he had never forged. Runeterra. A small thing, loud with prayer and 
            war, and strangely beloved by the celestial powers of Mount Targon. Those beings—Aspects 
            wearing divinity like armor—did what lesser creatures always do when faced with a miracle: 
            they tried to own it.
        </p>
