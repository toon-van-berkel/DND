        <p>
            Aurelion Sol did not learn creation—he remembered it. In the first breath of existence, 
            when the void was still deciding what it wanted to be, he drifted through the newborn 
            dark and seeded it with impossible lights. Each star was a signature, each constellation 
            a private joke, each comet an act of indulgent artistry. The cosmos was his gallery, and 
            he was its only critic.
        </p>
